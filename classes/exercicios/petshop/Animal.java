package exercicios.petshop;

public class Animal {
    Number Id;
    String Nome;
    String Raça;
    Number Peso;
    Number IdCategoria;
    Number IdTutor;

    public Animal(Number Id, String Nome, String Raça, Number Peso, Number IdCategoria, Number IdTutor) {
        this.Id = Id;
        this.Nome = Nome;
        this.Raça = Raça;
        this.Peso = Peso;
        this.IdCategoria = IdCategoria;
        this.IdTutor = IdTutor;
    }
}
